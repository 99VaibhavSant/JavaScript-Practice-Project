// Student's score
let score = 75;

// Check if score is 90 or above → Grade A
if (score >= 90) {
  console.log("A Grade");

// If score is 75–89 → Grade B
} else if (score >= 75) {
  console.log("B Grade");

// If score is 60–74 → Grade C
} else if (score >= 60) {
  console.log("C Grade");

// Anything below 60 → Fail
} else {
  console.log("Fail");
}
